    public void addRow() {
        try {
            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            ContributionService infc = (ContributionService) rg.lookup("contributionService");
            List<Contribution> sanzu = infc.contributions();
            if(sanzu!= null){
            for (Contribution snz : sanzu) {
                tbm.addRow(new Object[]{
                    snz.getContributionId(),
                    
                    snz.getAmount(),
                    snz.getDate()
                });
            }}else{
                System.out.println("Nodata found");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

