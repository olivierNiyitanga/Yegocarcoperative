    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (plateTxt.getText().trim().isEmpty() || amountTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "plz fill all data");
        } else {
            try {
                 // Create a Registry object and look up the MotariInterface object
                Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
                ContributionService infc = (ContributionService) rg.lookup("contributionService");

                // Create a Motari object and set its properties
                Contribution imisanzu = new Contribution();
                Driver mtr=new Driver(plateTxt.getText());
                imisanzu.setPlate(mtr);
                imisanzu.setDate(new java.util.Date());
                imisanzu.setAmount(Integer.parseInt(amountTxt.getText()));               

                // Call the addMotari() method and display the feedback
                String Fedback = infc.addContribution(imisanzu);
                if (!Fedback.isEmpty()) {
                    JOptionPane.showMessageDialog(this, Fedback);
                    tbm.setRowCount(0);
                    addRow();
                } else {
                    JOptionPane.showMessageDialog(this, "nost saved");
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

