    private void motariTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_motariTableMouseClicked
        DefaultTableModel tbm = (DefaultTableModel) motariTable.getModel();
        int index = motariTable.getSelectedRow();
        id = Integer.parseInt(tbm.getValueAt(index, 0).toString());
        try {
            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            AdminService infc = (AdminService) rg.lookup("admin");
            model.Admin admin = new model.Admin();
            admin.setAdminId(id);
            model.Admin admins = infc.searchById(admin);
            if (!admins.equals(null)) {
                nameTxt.setText(admins.getNames());
                usernameTxt.setText(admins.getUsername());
                passwordTxt.setText(admins.getPassword());
                phoneTxt.setText(admins.getPhone());
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_motariTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (nameTxt.getText().trim().isEmpty() || usernameTxt.getText().trim().isEmpty() || passwordTxt.getText().trim().isEmpty() || phoneTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "plz fill all data", "fill data error", JOptionPane.WARNING_MESSAGE);
        } else if (!phoneTxt.getText().matches(numberPtn)) {
            JOptionPane.showMessageDialog(this, "Letters not-+ allowed in phone number");
        } else if(phoneTxt.getText().length()!=10){
            JOptionPane.showMessageDialog(this, "phone number must be 10 number");
        }else if(!phoneTxt.getText().substring(0, 2).equals("07"))
        {
            JOptionPane.showMessageDialog(this, "phone number must start with 07*******");
        } 
        
        else if(passwordTxt.getText().length()<8 ){
            JOptionPane.showMessageDialog(this, "password can be like 8 or above characters");
        }
        
        else {
            try {
                Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
                AdminService infc = (AdminService) rg.lookup("adminService");
                model.Admin admin = new model.Admin();
                admin.setNames(nameTxt.getText());
                admin.setPassword(passwordTxt.getText());
                admin.setUsername(usernameTxt.getText());
                admin.setPhone(phoneTxt.getText());
                String Fedback = infc.addAdmin(admin);
                if (!Fedback.isEmpty()) {
                    JOptionPane.showMessageDialog(this, Fedback);
                    tbm.setRowCount(0);
                    addrows();
                    nameTxt.setText(null);
                usernameTxt.setText(null);
                passwordTxt.setText(null);
                phoneTxt.setText(null);
                
                } else {
                    JOptionPane.showMessageDialog(this, "nost saved");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

