    public void addrows() {
        try {
            Registry rg = LocateRegistry.getRegistry("127.0.0.1", 6000);
            AdminService infc = (AdminService) rg.lookup("adminService");
            List<model.Admin> motaris = infc.admins();
            if(motaris != null){
            for (model.Admin admn : motaris) {
                tbm.addRow(new Object[]{
                    admn.getAdminId(),
                    admn.getNames(),
                    admn.getUsername(),
                    admn.getPassword(),
                    admn.getPhone()
                });
            }}else{
                System.out.println("no data found");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

